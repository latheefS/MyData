---Creating Invoice Table---

CREATE TABLE XXMX_INVOICE_DOCUMENT_EXTRACTION 
(
    DOCUMENT_TYPE      VARCHAR2(250),
    INVOICE_NUMBER     VARCHAR2(20),
    INVOICE_DATE       DATE,
    VENDOR_NAME        VARCHAR2(2000),
    VENDOR_ADDRESS     VARCHAR2(2000),
    INVOICE_TOTAL      VARCHAR2(200),
    FILENAME           VARCHAR2(250),
    SEQUENCE_ID        NUMBER,
    SUB_TOTAL          VARCHAR2(200)
);
