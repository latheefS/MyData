-- Create Sequence for XXMX_INVOICE_DOCUMENT_EXTRACTION
CREATE SEQUENCE xxmx_invoice_seq
START WITH 1
INCREMENT BY 1
NOCACHE;
/

-- Create Trigger for XXMX_INVOICE_DOCUMENT_EXTRACTION
CREATE OR REPLACE TRIGGER xxmx_invoice_bir
BEFORE INSERT ON xxmx_invoice_document_extraction
FOR EACH ROW
BEGIN
    SELECT xxmx_invoice_seq.NEXTVAL
    INTO :NEW.sequence_id
    FROM dual;
END;
/
 
